# 操作字符串 提取信息
# 常规匹配符号
# \w 匹配字母数字下划线
# \W 匹配非字母数字下划线
# \s 匹配任意空白字符
# \S 匹配任意非空字符
# \d 匹配任意数字 [0-9]
# \D 匹配任意非数字字符
# \A 匹配字符串开头
# \Z 匹配字符串结尾,  存在换行的, 只匹配到换行前结束的字符串
# \z 匹配字符串结尾, 存在换行的匹配换行
# \n 匹配换行符
# \t 匹配制表符
# ^ 匹配字符串的开头
# $ 匹配字符串的结尾
# . 匹配任意字符除了换行符
# [xxx] 匹配 [] 中的字符
# [^xxx] 匹配非[]中的字符
# * 匹配0或者多个表达式
# + 匹配1个或者多个表达式
# ? 匹配0个或者1个前面的表达式
# {n} 匹配n个前面的表达式
# {n, m} 匹配n到m次前面的表达式片段 贪婪模式
# a|b 匹配a或者b
# (xx) 匹配括号内的表达式

